while True:
    user_input = input()

    if user_input == "CU":
        print("see you")
    elif user_input == ":-)":
        print("I’m happy")
    elif user_input == ":-(":
        print("I’m unhappy")
    elif user_input == ";-)":
        print("wink")
    elif user_input == ":-P":
        print("stick out my tongue")
    elif user_input == "(~.~)":
        print("sleepy")
    elif user_input == "TA":
        print("totally awesome")
    elif user_input == "CCC":
        print("Canadian Computing Competition")
    elif user_input == "CUZ":
        print("because")
    elif user_input == "TY":
        print("thank-you")
    elif user_input == "YW":
        print("you’re welcome")
    elif user_input == "TTYL":
        print("talk to you later")
        break
    else:
        print(user_input)